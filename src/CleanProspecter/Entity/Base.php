<?php

declare( strict_types = 1 );

namespace Solean\CleanProspecter\Entity;

abstract class Base
{
    private $id;

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param mixed $id
     */
    public function setId($id): void
    {
        $this->id = $id;
    }
}
